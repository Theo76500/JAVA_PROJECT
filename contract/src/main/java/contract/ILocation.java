package contract;

/**
 * The Interface ILocation
 *
 * @author LANGLOIS Theo
 */
public interface ILocation {
	
    /**
     * Getter the location y
     * @return
     * Get the location y
     */
    int getY();
    
    /**
     * Setter the location x
     * @param y
     * Location y
     */
    void setY(int y);
    
    /**
     * Getter X for the location
     * @return
     * Get the location X
     */
    int getX();
    
    /**
     * Setter X for the location
     * @param x
     * Set the location x
     */
    void setX(int x);

}
